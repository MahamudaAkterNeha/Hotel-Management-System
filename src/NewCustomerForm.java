    private void RegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RegisterActionPerformed
        // TODO add your handling code here:
        String CusName = Name.getText();
        String phoneNumber = PhoneNumber.getText();
        String email = Email.getText();
        String preAdd = PresentAddress.getText();
        String permAdd =PermanentAddress.getText();
        String age = Age.getText();
        String numofChildren = NumberofChildren.getText();
        String numofAdults = NumberofAdults.getText();
        String totalResidants = TotalResidant.getText();
        String checkinDate = CheckInDate.getText();
        String checkoutDate = CheckOutDate.getText();
        String totalDays =  txt5.getText();
        String roomType = txt2.getText();
        String bedType = txt3.getText();
        String totalCost = txt4.getText();
        String serialNumberofRoom = txt6.getText();
        String Confirmation = "Not Confirmed";
        
       
        
        String query1 = "INSERT INTO PENDINGCUSTOMER (\"NAME\", PHONENUMBER,EMAIL,PRESENTADDRESS,PERMANENTADDRESS,AGE,NUMBEROFCHILDREN,NUMBEROFADULTS,TOTALRESIDANTS,ROOMTYPE,BEDTYPE,TOTALNUMBEROFDAYS,CHECKINDATE,CHECKOUTDATE,TOTALCOST,SERIALNUMBEROFROOM,CONFIRMATION) VALUES (?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?)";
        try {
            PreparedStatement ps = con.prepareStatement(query1);
            ps.setString(1, CusName);
            ps.setString(2, phoneNumber);
            ps.setString(3, email);
            ps.setString(4, preAdd); 
            ps.setString(5, permAdd);
            ps.setString(6, age);
            ps.setString(7, numofChildren);
            ps.setString(8, numofAdults);
            ps.setString(9, totalResidants);
            ps.setString(10,roomType);
            ps.setString(11,bedType);
            ps.setString(12,totalDays);
            ps.setString(13,checkinDate);
            ps.setString(14,checkoutDate);
            ps.setString(15,totalCost);
            ps.setString(16,serialNumberofRoom);
            ps.setString(17,Confirmation);
        
            ps.executeUpdate();
        } catch (SQLException ex) {
            Logger.getLogger(NewCustomerForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_RegisterActionPerformed

