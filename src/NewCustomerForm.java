    private void CalculateTotalCostActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CalculateTotalCostActionPerformed
        // TODO add your handling code here:
        double TotalCost;
        
        String Roomtype;
        Roomtype = (String)RoomType.getSelectedItem();
        String Bedtype;
        Bedtype = (String)BedType.getSelectedItem();
        Double Days;
        Days = Double.parseDouble(txt5.getText());
        if("AC(per day 2000 BDT)".equals(Roomtype) && "Single(1000)".equals(Bedtype)){
          TotalCost=(2000+1000)*Days; 
          txt4.setText(String.valueOf(TotalCost));
        }
        
        if("AC(per day 2000 BDT)".equals(Roomtype) && "Double(1500)".equals(Bedtype)){
          TotalCost=(2000+1500)*Days; 
          txt4.setText(String.valueOf(TotalCost));
        }
        
        
        if("Non AC(per day 1000 BDT)".equals(Roomtype) && "Single(1000)".equals(Bedtype)){
          TotalCost=(1000+1000)*Days; 
          txt4.setText(String.valueOf(TotalCost));
        }
        
        if("Non AC(per day 1000 BDT)".equals(Roomtype) && "Double(1500)".equals(Bedtype)){
          TotalCost=(1000+1500)*Days; 
          txt4.setText(String.valueOf(TotalCost));
        }
        
        
    }//GEN-LAST:event_CalculateTotalCostActionPerformed
    
